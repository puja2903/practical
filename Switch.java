package Operator;

public class Switch {

	public static void main(String[] args) {
		int num=10;//declaring a variable for switch
		//switch expression
		switch(num) {
		//case statement
		case 1:System.out.println("12");
		break;
		case 12:System.out.println("15");
		break;
		case 10:System.out.println("very good");
		break;
		case 15:System.out.println("20");
		break;
		//default case statement
		default:System.out.println("the number is not within the range.");
		
		}

	}

}
